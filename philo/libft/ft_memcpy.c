#include "libft.h"

void	*ft_memcpy(void *dst, const void *src, size_t n)
{
	void	*tmp;

	if (!dst && !src)
		return (dst);
	tmp = dst;
	while (n--)
		*(char *)tmp++ = *(char *)src++;
	return (dst);
}
